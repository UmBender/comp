void solution() {
	ll n, k, x, y;
	cin >> n >> k >> x >> y;
	int first_night = min(n, k);
	int restn = n - first_night;
	int second_night = restn;
	dbg(first_night, second_night);
	cout << first_night * x + second_night * y << endl;
}

