void solution() {
    int n, m;
    cin >> n >> m;
    vector<string> grid(n);
    for (auto &i : grid) {
        cin >> i;
    }
    vector<int> lines(n), columns(m);
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if (grid[i][j] == '1') {
                lines[i]++;
                columns[j]++;
            }
        }
    }

    int counter_lines = 0, counter_columns = 0;
    for (auto &i : lines) {
        i %= 2;
        if (i) {
            counter_lines++;
        }
    }
    for (auto &j : columns) {
        j %= 2;
        if (j) {
            counter_columns++;
        }
    }
    cout << max(counter_columns, counter_lines) << endl;
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr), cout.tie(nullptr);
    // atcoder::modint::set_mod(998244353);
    int t;
    cin >> t;
    while (t--)
        solution();

    return 0;
}
