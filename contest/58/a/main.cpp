void solution() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    map<int, int> counter;
    counter[0] = 3;
    counter[1] = 1;
    counter[2] = 2;
    counter[3] = 1;
    counter[5] = 1;
    int crev   = 8;
    int pos    = 0;
    for (int i = 0; i < n; i++) {
        if (counter[a[i]] > 0) {
            counter[a[i]]--;
            crev--;
        }
        if (crev == 0) {
            pos = i;
            break;
        }
    }
    if (pos > 0) {
        pos++;
    }
    cout << pos << endl;
}

